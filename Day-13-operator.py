'''
There are different types of JavaScript operators:
Arithmetic Operators
Assignment Operators
Comparison Operators
String Operators
Logical Operators
Bitwise Operators
Ternary Operators
Type Operators
'''

x = 5
y = 2
print(x + y)   # 7
print(x - y)   # 3
print(x * y)   # 10
print(x / y)   # 2.5
print(x ** y)  # 25  [y এর value যত থাকবে ততবার x কে multiple করবে]
print(x // y)  # 2   [division এর মতোই কাজ just difference হলো এটি divide করে output কে floor করে ফেলবে।]

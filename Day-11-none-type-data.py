'''None is not defined to be 0, False, Empty string or any other value. In Python, None is an object.'''
x=None
print(x)         # None
print(type(x))   # NoneType

# None just None কারন False,string,0 এদের নিজস্ব data type রয়েছে। Other language এ None কে null বলে ।
A=False
print(type(A))   # bool

y=''
print(type(y))   # str

z=0
print(type(z))   # int


